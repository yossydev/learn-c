#include <bits/stdc++.h>
#include <iostream>
using namespace std;

int main() {
  int name;
  // nameに10を代入
  name = 10;
  cout << name << endl; // 10
  // nameに5を代入
  name = 5;
  cout << name << endl; // 5

  // 入力の受け取り方
  int a;
  // 変数aで入力を受け取る
  cin >> a;               // 5が入力値
  cout << a * 10 << endl; // 50が出力値
}
